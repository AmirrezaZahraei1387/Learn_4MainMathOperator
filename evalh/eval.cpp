namespace precalc {

    int add(int number_1, int number_2) {
        return number_2 + number_1;
    }


int sub(int number_1, int number_2) {
        return number_1 - number_2;
    }

int dev(int number_1, int number_2) {
        return number_1 / number_2;
    }

int mul(int number_1, int number_2) {
        return number_1 * number_2;
    }
}// precalc